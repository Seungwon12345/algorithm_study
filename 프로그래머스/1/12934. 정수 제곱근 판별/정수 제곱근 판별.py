def solution(n):
    # n의 제곱근을 계산합니다.
    # n이 121이면 x는 11.0, n이 97이면 x는 9.848...
    x = n**0.5

    # x가 정수인지 확인합니다.
    # `x == int(x)`는 x의 소수점 아래가 0인지 확인하는 가장 쉬운 방법입니다.
    if x == int(x):
        # 정수라면, (x+1)의 제곱을 반환합니다.
        return (x + 1) ** 2
    else:
        # 정수가 아니라면 -1을 반환합니다.
        return -1